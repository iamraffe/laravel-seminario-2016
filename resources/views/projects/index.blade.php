              <p>Minimiza errores de prescripción, dispensación e interpretación de receta y está disponible para el 100% de la población del País Vasco que a través de la Hoja de Tratamiento Activo, dispone de toda la información desde la Carpeta de Salud  del Ciudadano, favoreciendo la adherencia.</p>

              <p>El HFT permite un nuevo modelo de atención al pasar de receta a plan terapéutico favoreciendo la atención integral al paciente. Facilita la toma de decisiones al clínico, optimiza el tratamiento y favorece la conciliación, continuidad, seguridad  y calidad asistenciales, reduciendo las incidencias de medicación y los tratamientos activos en el ámbito comunitario,  contribuyendo a la sostenibilidad del sistema.</p>
