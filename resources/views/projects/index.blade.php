            <p>La terapia intravenosa constituye uno de los riesgos principales de seguridad a los que se expone un recién nacido durante su ingreso hospitalario. Dos de los problemas principales son las infecciones relacionadas con el uso de catéteres intravenosos y los acontecimientos relacionados con la propia medicación intravenosa administrada, como son los efectos adversos de los fármacos y los errores humanos durante la administración de medicación. El proyecto presentado se planteó en dos fases y su finalidad fue la de aumentar la seguridad durante la terapia intravenosa en una Unidad de Neonatología de 3<sup>er</sup> nivel.</p>
